    private void buttonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSubmitActionPerformed
        String idNum = fieldIdNum.getText();
        
        int genderIndex = comboGender.getSelectedIndex();
        Gender gender = Gender.values()[genderIndex];        
        
        String firstName = fieldFirstName.getText();
        String middleName = fieldMiddleName.getText();
        String lastName = fieldLastName.getText();
        FullName fullName = new FullName(firstName, middleName, lastName);
        
        int birthMonth = comboMonth.getSelectedIndex();
        int birthDate = comboDate.getSelectedIndex() + 1;
        int birthYear = comboYear.getSelectedIndex() + 80;
        Date birthdate = new Date(birthYear, birthMonth, birthDate);
        
        String jobTitle = fieldJobTitle.getSelectedText();
        
        Registrar newRegistrar = new Registrar(idNum, fullName, birthdate, gender, jobTitle);
        Accounts.registrars.add(newRegistrar);
    }//GEN-LAST:event_buttonSubmitActionPerformed


    void AddColumnListener() {
        JTableHeader header = tableSubjectList.getTableHeader();
        header.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e){
                int columnIndex = header.columnAtPoint(e.getPoint());
                System.out.println("Column " + columnIndex + " is clicked.");
                
                switch(columnIndex){
                    case 0 -> {
                        enrolledSubjects = Subjects.sortCode(enrolledSubjects, descending);
                        descending = !descending;
                    }
                    case 1 -> {
                        enrolledSubjects = Subjects.sortName(enrolledSubjects, descending);
                        descending = !descending;
                    }
                    case 2 -> {
                        enrolledSubjects = Subjects.sortTeacher(enrolledSubjects, descending);
                        descending = !descending;
                    }
                }
                SetTableView();
            }
        });
    }


    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        String subjectCode = fieldDeleteSubjectCode.getText();
        
        for(int i = 0; i < enrolledSubjects.size(); i++){
            if(enrolledSubjects.get(i).getSubjectCode().equals(subjectCode)){
                enrolledSubjects.remove(i);
            }
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

    private void buttonDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonDeleteMouseClicked
        String subjectCode = fieldDeleteSubjectCode.getText();
        
        if (subjectCode == ""){
            javax.swing.JFrame failedDelete = new javax.swing.JFrame();
            javax.swing.JOptionPane.showMessageDialog(failedDelete, "You have a blank field! Please fill up all fields.");
        } else {
            for(int i = 0; i < enrolledSubjects.size(); i++){
                if(enrolledSubjects.get(i).getSubjectCode().equals(subjectCode)){
                    enrolledSubjects.remove(i);
                }
            }
            javax.swing.JFrame successfulDelete = new javax.swing.JFrame();
            javax.swing.JOptionPane.showMessageDialog(successfulDelete, "Subject successfully deleted!");
        }
    }//GEN-LAST:event_buttonDeleteMouseClicked

